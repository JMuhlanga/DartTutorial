/// Data Types
/* 
Strongly Typed Language: The type of a variable is known at compile time.
For example: C++, JAVA, SWIFT

Dynamic Typed: The type of a variable is known at Run time.
For example: Python, Ruby, JavaScript.
*/

main(){
  /* 
  int
  double 
  String
  bool
  dynamic
  */

  int amount1 = 100;
  var amount2 = 200;

  print('Amount!: $amount1 | Amount2: $amount2 \n');

  double dAmount1 = 100.11;
  var dAmount2 = 200.22;

  print('dAmount1: $dAmount1 | dAmount2: $dAmount2');

  String name1 = 'Muhlanga';
  var name2 = 'Kupeka';

  print('My name is : $name1 $name2 \n ');

  bool isItTrue1 = true;
  var isItTrue2 = false;

print('isItTrue1: $isItTrue1 | isItTrue2: $isItTrue2 \n');

dynamic weakVariable = 100;
print('WeakVariable 1: $weakVariable \n');

}